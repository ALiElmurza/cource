package lesson4.HomeWork.Task3and5;

public class Movie {
    int year;
    String name;
    double rating;
    String genre;
    String country;
    boolean oscar;


    public Movie(int year, String name, double rating, String genre, String country, boolean oscar) {
        this.year = year;
        this.name = name;
        this.rating = rating;
        this.genre = genre;
        this.country = country;
        this.oscar = oscar;
    }

}

